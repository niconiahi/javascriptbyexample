// an _object_ allows you to group pieces of data to represent something

// _objects_ are composed of _key-value pairs_
const object = {
  key: "value",
}
console.log("object", object)

// "person" is an _object_ which has two _keys_: "name" which _value_ is "Mario" and "surname" which _value_ is "Baracuz"
const person = {
  name: "Mario",
  surname: "Baracuz",
}
console.log("person", person)
